void TEST_CASES()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];
    int ans = INT_MAX;

    for (int i = 0; i <= n; i++)
    {
        int mx = 0;
        if (i)
            mx = max(mx, a[i - 1] - 1);
        if (i != n)
            mx = max(mx, 1000000 - a[i]);
        ans = min(ans, mx);
    }
    cout << ans << "\n";
}

