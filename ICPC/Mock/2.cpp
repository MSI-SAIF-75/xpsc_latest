void TEST_CASES()
{
    int n, a, b;
    cin >> n >> a >> b;
    vector<int> v(n);
    for (int i = 0; i < n; i++)
        cin >> v[i];
    sort(v.begin(), v.end());
    int ans = 0;
    for (int i = 0; i < n; i++)
    {
        for (int j = i + 1; j < n; j++)
        {
            int x = v[i] + v[j];
            if (a <= x && x <= b)
                ans++;
        }
    }
    cout << ans << "\n";
}

