void TEST_CASES()
{
    ll n;
    cin >> n;
    vector<ll> a(n);
    ll sum = 0;
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
        sum += a[i];
    }
    ll ans = INT_MAX;
    for (int mask = 0; mask < 1 << n; mask++)
    {
        ll tumb = 0;
        for (int i = 0; i < n; i++)
        {
            if (mask & 1 << i)
                tumb += a[i];
        }

        if (abs(sum - 2 * tumb) < ans)
            ans = abs(sum - 2 * tumb);
    }
    cout << ans << "\n";
}

